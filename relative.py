first = int(input("첫번째 정수 : "))
second = int(input("두번째 정수 :"))

print("{} > {} = {}".format(first,second,(first>second)))
print("{} >= {} = {}".format(first,second,(first>=second)))
print("{} < {} = {}".format(first,second,(first<second)))
print("{} <= {} = {}".format(first,second,(first<=second)))
print("{} == {} = {}".format(first,second,(first==second)))
print("{} != {} = {}".format(first,second,(first!=second)))
#비교 혹은 관계 연산자
#두 수의 대소 동등 관계를 계산하여 논리값을 내어주는 연산
